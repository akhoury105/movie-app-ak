from flask import Flask, jsonify
from models import setup_db
import json

def create_app():

    app = Flask(__name__)
    setup_db(app)

    return app

app = create_app()


@app.errorhandler(422)
def unprocessable(error):
    return jsonify({
        "success": False,
        "error": 422,
        "message": "unprocessable"
    }), 422


@app.errorhandler(404)
def not_found(error):
    return jsonify({
        "success": False,
        "error": 404,
        "message": "resource not found"
    }), 404


@app.errorhandler(400)
def bad_request(error):
    return jsonify({
        "success": False,
        "error": 400,
        "message": "bad request"
    })


if __name__ == '__main__':
    app.run()